function changeClass() {
    const rbox = document.getElementsByClassName('rbox');

    // Check if the element has 'new-class'
    if (rbox.classList.contains('on')) {
        // Remove 'new-class' and add 'initial-class'
        rbox.classList.remove('on');
        rbox.classList.add('rbox');
    } else {
        // Remove 'initial-class' and add 'new-class'
        rbox.classList.remove('rbox');
        rbox.classList.add('on');
    }
}

